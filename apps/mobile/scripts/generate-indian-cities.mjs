const MAX_RESULTS = 50;

export function searchCities(query: string): City[] {
  const q = query.trim().toLowerCase();
  if (q.length < 2) return [];
  const scored: { city: City; score: number }[] = [];
  for (const c of INDIAN_CITIES) {
    const n = c.name.toLowerCase();
    const s = c.state.toLowerCase();
    if (n === q) {
      scored.push({ city: c, score: 0 });
      continue;
    }
    if (n.startsWith(q)) {
      scored.push({ city: c, score: 1 });
      continue;
    }
    if (n.includes(q)) {
      scored.push({ city: c, score: 2 });
      continue;
    }
    if (s.includes(q) || q.includes(n.slice(0, Math.min(4, n.length)))) {
      scored.push({ city: c, score: 4 });
    }
  }
  scored.sort((a, b) => a.score - b.score || a.city.name.localeCompare(b.city.name));
  const out: City[] = [];
  const dedupe = new Set<string>();
  for (const { city } of scored) {
    const k = city.name + "|" + city.state;
    if (dedupe.has(k)) continue;
    dedupe.add(k);
    out.push(city);
    if (out.length >= MAX_RESULTS) break;
  }
  return out;
}
